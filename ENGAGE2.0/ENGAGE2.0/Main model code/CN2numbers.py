    def get_SCSCN2_numbers(self, soil, soil_type, land_cover, land_cover_type):

        if soil_type == "HOST":
            # Method for calculating the CN2 number from HOST data
            def get_soil_letters(a1):
                if a1 >= 0:
                    return self.HOST_SCS_soil[a1]
                else:
                    return 'N'
            V_get_SOIL = np.vectorize(get_soil_letters)

            SCS_soil = np.empty_like(soil, dtype = str)

            SCS_soil = V_get_SOIL(soil)
            arcpy.AddMessage("SCS soil groups calculated for HOST soil data")
            arcpy.AddMessage("-------------------------")

        # Method for calculating the CN2 number from FAO data
        elif soil_type == "FAO":
            def get_soil_letters(a1):
                if a1 >= 0:
                    return self.FAO_soil[a1]
                else:
                    return 'N'

            V_get_SOIL = np.vectorize(get_soil_letters)

            SCS_soil = np.empty_like(soil, dtype = str)

            SCS_soil = V_get_SOIL(soil)
            arcpy.AddMessage("SCS soil groups calculated for FAO soil data")
            arcpy.AddMessage("-------------------------")

        # Method for calculating the CN2 number from LCM2007 data
        if land_cover_type == "LCM 2007":
            def get_CN2_numbers(a1, a2):
                if a1 > 0 and a2 != 'N':
                    return self.COMBINE_SCS[(a1, a2)]  
                else:
                    return -9999

            V_get_CN = np.vectorize(get_CN2_numbers)
        
            CN2_d = np.zeros_like(land_cover, dtype = np.int8)

            CN2_d = V_get_CN(land_cover, SCS_soil)
            arcpy.AddMessage("CN2 calculated for LCM 2007 land cover data")
            arcpy.AddMessage("-------------------------")

        # Method for calculating the CN2 number from CORINE data
        elif land_cover_type == "CORINE 2006":
            def get_CN2_numbers(a1, a2):
                if a1 > 0 and a2 != 'N':
                    return self.Corine[(a1, a2)] 
                else:
                    return -9999

            V_get_CN = np.vectorize(get_CN2_numbers)
        
            CN2_d = np.zeros_like(land_cover, dtype = np.int8)

            CN2_d = V_get_CN(land_cover, SCS_soil)
